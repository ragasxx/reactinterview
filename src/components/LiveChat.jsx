function generate() {
  var finalName = nameList[Math.floor(Math.random() * nameList.length)];
  return finalName;
}

const Chat_Limit = 20;

const LiveChat = () => {
  const [messages, setMessages] = useState([]);

  const fetchData = () => {
    const data = [
      {
        id: Math.random() * 100,
        name: generate(),
        message: "Hello this is my first comment",
        photo: "https://i.redd.it/88qjrepdhng61.jpg",
      },
    ];

    setMessages((prevMessages) => {
      let newMessageList = [...data, ...prevMessages];
      newMessageList = newMessageList.splice(0, Chat_Limit);
      return newMessageList;
    });
  };

  useEffect(() => {
    const s = setInterval(fetchData, 1000);
    return () => clearInterval(s);
  }, []);

  console.log(messages);

  return (
    <div className="w-full  bg-gradient-to-b from-black to-gray-900 min-h-screen flex  justify-center items-center">
      <div className="border border-slate-700 flex w-[30vw] h-[80vh] flex-col-reverse mb-auto overflow-y-auto scroll-smooth">
        {messages.map((message) => (
          <ChatMessage
            key={message.id}
            name={message.name}
            photo={message.photo}
            message={message.message}
          />
        ))}
      </div>
    </div>
  );
};

export default LiveChat;
